"""Peak-memory measurements for the read paths, written to MEMORY.md.

pytest-benchmark measures time only, and tracemalloc cannot see the
parser's allocations (they happen in Rust), so this measures peak RSS via
`getrusage` instead. Each case runs in a fresh child process; the read
delta is the growth in peak RSS attributable to the read itself, after
imports and fixture I/O have already peaked.

    uv run benchmarks/memory.py

Numbers are only comparable within one run on one machine. The streamed
rows (`oxyz-iter`, `ase-iread`) discard frames as they go — their delta is
the constant-memory claim; the read-all rows keep every frame alive.
"""

from __future__ import annotations

import json
import platform
import resource
import subprocess
import sys
from pathlib import Path

OUTPUT = Path(__file__).resolve().parent / "MEMORY.md"


def peak_rss_bytes() -> int:
    peak = resource.getrusage(resource.RUSAGE_SELF).ru_maxrss
    # ru_maxrss is bytes on macOS, kibibytes on Linux.
    return peak if sys.platform == "darwin" else peak * 1024


def oxyz_read_all(path: Path) -> int:
    import oxyz

    return len(oxyz.read(path))


def oxyz_read_all_serial(path: Path) -> int:
    import oxyz

    return len(oxyz.read(path, threads=1))


def oxyz_iter(path: Path) -> int:
    import oxyz

    return sum(1 for _ in oxyz.iread(path))


def ase_read_all(path: Path) -> int:
    from ase.io import read

    frames = read(path, index=":", format="extxyz")
    assert isinstance(frames, list)
    return len(frames)


def ase_iread(path: Path) -> int:
    from ase.io import iread

    return sum(1 for _ in iread(path, format="extxyz"))


READERS = {
    "oxyz": (oxyz_read_all, "all frames"),
    "oxyz-serial": (oxyz_read_all_serial, "all frames"),
    "oxyz-iter": (oxyz_iter, "streamed"),
    "ase": (ase_read_all, "all frames"),
    "ase-iread": (ase_iread, "streamed"),
}


def child(reader: str, path: Path) -> None:
    """Run one case and print its peak/delta as JSON on the last line."""
    read, _ = READERS[reader]
    before = peak_rss_bytes()
    n_frames = read(path)
    after = peak_rss_bytes()
    print(
        json.dumps(
            {
                "n_frames": n_frames,
                "peak_mib": after / 2**20,
                "delta_mib": (after - before) / 2**20,
            }
        )
    )


def measure(reader: str, path: Path) -> dict:
    result = subprocess.run(
        [sys.executable, __file__, "--child", reader, str(path)],
        capture_output=True,
        text=True,
        check=True,
    )
    return json.loads(result.stdout.splitlines()[-1])


def main() -> None:
    import oxyz._rust

    if oxyz._rust.__build_profile__ != "release":
        sys.exit("oxyz._rust is a debug build; run: uv run maturin develop --release")

    sys.path.insert(0, str(Path(__file__).resolve().parent))
    from conftest import trajectory_files

    fixtures = trajectory_files()
    workloads = ["many_small_frames", "large_frames"]

    lines = [
        "# Peak memory",
        "",
        "Generated by `benchmarks/memory.py` — do not edit by hand.",
        "",
        "Peak RSS per case, each in a fresh process. The read delta is peak",
        "growth during the read itself (imports and fixture access excluded);",
        "streamed rows discard frames as they go, read-all rows keep them.",
        "",
        f"- {platform.platform()}, CPython {platform.python_version()}",
        "",
    ]
    for workload in workloads:
        path = fixtures[workload]
        size_mib = path.stat().st_size / 2**20
        lines += [
            f"## {workload} ({size_mib:.1f} MiB file)",
            "",
            "| reader | output kept | peak RSS (MiB) | read delta (MiB) |",
            "| --- | --- | ---: | ---: |",
        ]
        for reader, (_, kept) in READERS.items():
            stats = measure(reader, path)
            lines.append(
                f"| {reader} | {kept} | {stats['peak_mib']:.1f}"
                f" | {stats['delta_mib']:.1f} |"
            )
            print(f"{workload}/{reader}: {stats['delta_mib']:.1f} MiB delta")
        lines.append("")

    OUTPUT.write_text("\n".join(lines))
    print(f"wrote {OUTPUT}")


if __name__ == "__main__":
    if len(sys.argv) > 1 and sys.argv[1] == "--child":
        child(sys.argv[2], Path(sys.argv[3]))
    else:
        main()
