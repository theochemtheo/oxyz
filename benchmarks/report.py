# Group-name prefixes that render under their own intro section; anything else
# is a plain reader group.
SECTION_PREFIXES = ("stores", "scaling", "real_data")


def bucket_groups(
    groups: dict[str, list[dict[str, Any]]],
) -> dict[str, dict[str, list[dict[str, Any]]]]:
    """Split groups into the sections RESULTS.md renders, by name prefix."""
    buckets: dict[str, dict[str, list[dict[str, Any]]]] = {
        name: {} for name in (*SECTION_PREFIXES, "readers")
    }
    for group, rows in groups.items():
        section = next(
            (prefix for prefix in SECTION_PREFIXES if group.startswith(prefix)),
            "readers",
        )
        buckets[section][group] = rows
    return buckets


def natural_key(group: str) -> tuple:
    """Sort scaling_dataset/1000 before /10000 numerically, not lexically."""
    scenario, _, size = group.partition("/")
    return (scenario, int(size) if size.isdigit() else 0, size)


def newest_save() -> Path:
    saves = sorted(
        (REPO / ".benchmarks").rglob("*.json"), key=lambda p: p.stat().st_mtime
    )
    if not saves:
        sys.exit("no saves under .benchmarks/ — run with --benchmark-autosave first")
    return saves[-1]


def row_id(bench: dict[str, Any]) -> str:
    if bench.get("param"):
        return bench["param"]
    name = bench["name"]
    return name[name.index("[") + 1 : -1] if "[" in name else name


def fmt_ms(ms: float) -> str:
    if ms < 10:
        return f"{ms:.2f}"
    if ms < 100:
        return f"{ms:.1f}"
    return f"{ms:.0f}"


def fmt_rate(per_s: float) -> str:
    if per_s >= 1e9:
        return f"{per_s / 1e9:.2f}G"
    if per_s >= 1e6:
        return f"{per_s / 1e6:.2f}M"
    if per_s >= 1e3:
        return f"{per_s / 1e3:.1f}k"
    return f"{per_s:.0f}"


def throughput(bench: dict[str, Any], key: str) -> str:
    """frames/s or atoms/s from the row's recorded workload shape; blank
    for rows whose cost does not scale with frames read (first/last/scan)."""
    count = bench.get("extra_info", {}).get(key)
    if count is None:
        return ""
    return fmt_rate(count / bench["stats"]["mean"])


def environment(data: dict[str, Any]) -> str:
    machine = data["machine_info"]
    commit = data.get("commit_info", {})
    cpu = machine.get("cpu", {}).get("brand_raw", machine.get("processor", "?"))
    versions = machine.get("package_versions", {})
    installed = ", ".join(
        f"{dist} {version}" for dist, version in versions.items() if version
    )

    lines = [
        "## Environment",
        "",
        f"- {cpu}, {machine.get('system', '?')} {machine.get('release', '?')}"
        f" ({machine.get('machine', '?')})",
        f"- CPython {machine.get('python_version', '?')}",
    ]
    if installed:
        lines.append(f"- {installed}")
    when = data.get("datetime", "?")[:10]
    commit_id = commit.get("id", "?")[:9]
    dirty = ", uncommitted changes" if commit.get("dirty") else ""
    lines.append(f"- run {when}, commit {commit_id}{dirty}")
    return "\n".join(lines) + "\n"


def group_table(group: str, rows: list[dict[str, Any]]) -> str:
    rows = sorted(rows, key=lambda bench: bench["stats"]["mean"])
    best = rows[0]["stats"]["mean"]
    lines = [
        f"### {group}",
        "",
        "| reader | output | mode | mean ± std (ms) | frames/s | atoms/s | rel |",
        "| --- | --- | --- | ---: | ---: | ---: | ---: |",
    ]
    for bench in rows:
        stats = bench["stats"]
        info = bench.get("extra_info", {})
        timing = f"{fmt_ms(stats['mean'] * 1e3)} ± {fmt_ms(stats['stddev'] * 1e3)}"
        lines.append(
            f"| {row_id(bench)} | {info.get('output', '?')}"
            f" | {info.get('mode', '?')} | {timing}"
            f" | {throughput(bench, 'n_frames')} | {throughput(bench, 'n_atoms')}"
            f" | {stats['mean'] / best:.2f} |"
        )
    return "\n".join(lines) + "\n"


def not_led_by_oxyz(groups: dict[str, list[dict[str, Any]]]) -> str:
    # Competitor rows are the other readers and the stores; everything
    # else (including oxyz-only groups with descriptive ids) is ours.
    def competitor(bench: dict[str, Any]) -> bool:
        return row_id(bench).startswith(("ase", "cextxyz", "atompack", "lmdb"))

    losses = []
    for group, rows in sorted(groups.items()):
        fastest = min(rows, key=lambda bench: bench["stats"]["mean"])
        if not competitor(fastest):
            continue
        ours = [bench for bench in rows if not competitor(bench)]
        if ours:
            best = min(ours, key=lambda bench: bench["stats"]["mean"])
            margin = best["stats"]["mean"] / fastest["stats"]["mean"]
            losses.append(
                f"- {group}: `{row_id(fastest)}` leads; oxyz's best row is"
                f" {margin:.1f}× slower"
            )
        else:
            losses.append(f"- {group}: `{row_id(fastest)}` leads; no oxyz row")
    if not losses:
        return "No groups in this run were led by a non-oxyz row.\n"
    return (
        "Groups where an oxyz row is not the fastest. For store groups this"
        " is the expected outcome — binary stores against a text parser —"
        " and the margin is the result:\n\n" + "\n".join(losses) + "\n"
    )


def main() -> None:
    save = Path(sys.argv[1]) if len(sys.argv) > 1 else newest_save()
    data = json.loads(save.read_text())

    groups: dict[str, list[dict[str, Any]]] = {}
    for bench in data["benchmarks"]:
        groups.setdefault(bench["group"] or "ungrouped", []).append(bench)

    buckets = bucket_groups(groups)

    parts = [
        INTRO.format(save=save.name),
        environment(data),
        WORKLOADS,
        "## Results\n",
        *(
            group_table(group, rows)
            for group, rows in sorted(buckets["readers"].items())
        ),
    ]
    if buckets["real_data"]:
        parts += [
            REAL_DATA_INTRO,
            *(
                group_table(group, rows)
                for group, rows in sorted(buckets["real_data"].items())
            ),
        ]
    if buckets["stores"]:
        parts += [
            STORES_INTRO,
            *(
                group_table(group, rows)
                for group, rows in sorted(buckets["stores"].items())
            ),
        ]
    if buckets["scaling"]:
        ordered = sorted(buckets["scaling"].items(), key=lambda kv: natural_key(kv[0]))
        parts += [
            SCALING_INTRO,
            *(group_table(group, rows) for group, rows in ordered),
        ]
    parts += [
        "## Where oxyz is not fastest\n",
        not_led_by_oxyz(groups),
    ]
    RESULTS.write_text("\n".join(parts))
    print(f"wrote {RESULTS} from {save.name}")


if __name__ == "__main__":
    main()
