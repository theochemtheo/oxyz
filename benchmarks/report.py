"""Render benchmarks/RESULTS.md from a saved pytest-benchmark run.

Usage:
    uv run benchmarks/report.py [SAVE.json]

Stdlib only; uv runs it anywhere. Without an argument, the newest save
under .benchmarks/ is used. The tables, row descriptions, and environment
section all come from the JSON (rows carry their output/mode via
extra_info, versions via a conftest hook); only the prose lives here.
Regenerate after any run worth recording:

    uv run benchmarks/run.py --benchmark-autosave
    uv run benchmarks/report.py
"""

from __future__ import annotations

import json
import sys
from pathlib import Path
from typing import Any

REPO = Path(__file__).resolve().parent.parent
RESULTS = Path(__file__).resolve().parent / "RESULTS.md"

INTRO = """\
# Benchmark results

Generated by `benchmarks/report.py` from `{save}` — do not edit by hand.

The output column says what each row materialises: `numpy frames` are
frame objects holding numpy arrays (oxyz's native form, and cextxyz's via
its `read_dicts` interface), `ase.Atoms` are full Atoms objects (ASE's
own reader, `oxyz.ase.read`, and the C parser behind ASE's IO via the
ase-extxyz plugin), and `Batch` is a single concatenated batch. Rows with
different outputs do different amounts of work. The mode column says
whether parsing uses all cores or one.

"rel" is relative to the fastest row in the group. Numbers are only
comparable within one run on one machine.
"""

WORKLOADS = """\
## Workloads

Deterministic fixtures from `benchmarks/conftest.py`:

| file | shape |
| --- | --- |
| many_small_frames | 2 000 frames × 16–64 atoms |
| large_frames | 4 frames × 100 000 atoms |
| metadata_heavy | 2 000 frames × 16–64 atoms, ~16-key comment lines |
| mace_mixed | 5 isolated-atom frames + 995 bulk frames (32–96 atoms) |

`selective/...` groups read every 20th frame. cextxyz and ase-extxyz
publish no CPython 3.14 wheels, so `benchmarks/run.py` pins CPython 3.13;
oxyz ships the same abi3 wheel for 3.13 and 3.14.
"""


def newest_save() -> Path:
    saves = sorted(
        (REPO / ".benchmarks").rglob("*.json"), key=lambda p: p.stat().st_mtime
    )
    if not saves:
        sys.exit("no saves under .benchmarks/ — run with --benchmark-autosave first")
    return saves[-1]


def row_id(bench: dict[str, Any]) -> str:
    if bench.get("param"):
        return bench["param"]
    name = bench["name"]
    return name[name.index("[") + 1 : -1] if "[" in name else name


def fmt_ms(ms: float) -> str:
    if ms < 10:
        return f"{ms:.2f}"
    if ms < 100:
        return f"{ms:.1f}"
    return f"{ms:.0f}"


def environment(data: dict[str, Any]) -> str:
    machine = data["machine_info"]
    commit = data.get("commit_info", {})
    cpu = machine.get("cpu", {}).get("brand_raw", machine.get("processor", "?"))
    versions = machine.get("package_versions", {})
    installed = ", ".join(
        f"{dist} {version}" for dist, version in versions.items() if version
    )

    lines = [
        "## Environment",
        "",
        f"- {cpu}, {machine.get('system', '?')} {machine.get('release', '?')}"
        f" ({machine.get('machine', '?')})",
        f"- CPython {machine.get('python_version', '?')}",
    ]
    if installed:
        lines.append(f"- {installed}")
    when = data.get("datetime", "?")[:10]
    commit_id = commit.get("id", "?")[:9]
    dirty = ", uncommitted changes" if commit.get("dirty") else ""
    lines.append(f"- run {when}, commit {commit_id}{dirty}")
    return "\n".join(lines) + "\n"


def group_table(group: str, rows: list[dict[str, Any]]) -> str:
    rows = sorted(rows, key=lambda bench: bench["stats"]["mean"])
    best = rows[0]["stats"]["mean"]
    lines = [
        f"### {group}",
        "",
        "| reader | output | mode | mean ± std (ms) | rel |",
        "| --- | --- | --- | ---: | ---: |",
    ]
    for bench in rows:
        stats = bench["stats"]
        info = bench.get("extra_info", {})
        timing = f"{fmt_ms(stats['mean'] * 1e3)} ± {fmt_ms(stats['stddev'] * 1e3)}"
        lines.append(
            f"| {row_id(bench)} | {info.get('output', '?')}"
            f" | {info.get('mode', '?')} | {timing}"
            f" | {stats['mean'] / best:.2f} |"
        )
    return "\n".join(lines) + "\n"


def not_led_by_oxyz(groups: dict[str, list[dict[str, Any]]]) -> str:
    # Competitor rows are `ase`, `cextxyz`, `cextxyz-to-ase`; everything
    # else (including oxyz-only groups with descriptive ids) is ours.
    def competitor(bench: dict[str, Any]) -> bool:
        return row_id(bench).startswith(("ase", "cextxyz"))

    losses = []
    for group, rows in sorted(groups.items()):
        fastest = min(rows, key=lambda bench: bench["stats"]["mean"])
        if competitor(fastest):
            losses.append(f"- {group}: fastest row is `{row_id(fastest)}`")
    if not losses:
        return "No groups in this run were led by a non-oxyz row.\n"
    return "Groups where an oxyz row is not the fastest:\n\n" + "\n".join(losses) + "\n"


def main() -> None:
    save = Path(sys.argv[1]) if len(sys.argv) > 1 else newest_save()
    data = json.loads(save.read_text())

    groups: dict[str, list[dict[str, Any]]] = {}
    for bench in data["benchmarks"]:
        groups.setdefault(bench["group"] or "ungrouped", []).append(bench)

    parts = [
        INTRO.format(save=save.name),
        environment(data),
        WORKLOADS,
        "## Results\n",
        *(group_table(group, rows) for group, rows in sorted(groups.items())),
        "## Where oxyz is not fastest\n",
        not_led_by_oxyz(groups),
    ]
    RESULTS.write_text("\n".join(parts))
    print(f"wrote {RESULTS} from {save.name}")


if __name__ == "__main__":
    main()
